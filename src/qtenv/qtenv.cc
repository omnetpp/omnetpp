void Qtenv::bubble(cComponent *component, const char *text)
{
    EnvirBase::bubble(component, text);

    if (!opt->showBubbles)
        return;

    if (component->getParentModule()) {
        cModule *enclosingmod = component->getParentModule();
        for (auto inspector : inspectors)
            if (ModuleInspector *insp = isModuleInspectorFor(enclosingmod, inspector))
                insp->bubble(component, text);
    }
}

void Qtenv::confirm(DialogKind kind, const char *msg)
{
    if (!mainWindow) {
        // fallback in case Qt didn't fire up correctly
        const char *prefix = kind==ERROR ? "Error: " : kind==WARNING ? "Warning: " : "";
        out << "\n<!> " << prefix << msg << endl << endl;
    }
    else {
        switch (kind) {
        case INFO: QMessageBox::information(mainWindow, "Confirm", msg, QMessageBox::StandardButton::Ok); break;
        case WARNING: QMessageBox::warning(mainWindow, "Warning", msg, QMessageBox::StandardButton::Ok); break;
        case ERROR: QMessageBox::critical(mainWindow, "Error", msg, QMessageBox::StandardButton::Ok); break;
        }
    }
}

